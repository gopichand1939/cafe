CREATE TABLE IF NOT EXISTS message_settings (
  id SERIAL PRIMARY KEY,
  admin_id INTEGER NOT NULL UNIQUE REFERENCES admin(id) ON DELETE CASCADE,
  admin_email VARCHAR(255) NOT NULL,
  sender_name VARCHAR(255) NOT NULL DEFAULT 'Bagel Master Cafe',
  sender_email VARCHAR(255) NOT NULL DEFAULT '',
  smtp_host VARCHAR(255) NOT NULL DEFAULT '',
  smtp_port INTEGER NOT NULL DEFAULT 587,
  smtp_secure SMALLINT NOT NULL DEFAULT 0,
  smtp_user VARCHAR(255) NOT NULL DEFAULT '',
  smtp_pass TEXT NOT NULL DEFAULT '',
  mail_enabled SMALLINT NOT NULL DEFAULT 0,
  notify_customer_created SMALLINT NOT NULL DEFAULT 1,
  notify_customer_updated SMALLINT NOT NULL DEFAULT 1,
  notify_customer_deleted SMALLINT NOT NULL DEFAULT 1,
  notify_order_created SMALLINT NOT NULL DEFAULT 1,
  notify_order_updated SMALLINT NOT NULL DEFAULT 1,
  notify_order_deleted SMALLINT NOT NULL DEFAULT 1,
  notify_customer_mail_customer_created SMALLINT NOT NULL DEFAULT 1,
  notify_customer_mail_customer_updated SMALLINT NOT NULL DEFAULT 1,
  notify_customer_mail_customer_deleted SMALLINT NOT NULL DEFAULT 1,
  notify_customer_mail_order_created SMALLINT NOT NULL DEFAULT 1,
  notify_customer_mail_order_updated SMALLINT NOT NULL DEFAULT 1,
  notify_customer_mail_order_deleted SMALLINT NOT NULL DEFAULT 1,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);
